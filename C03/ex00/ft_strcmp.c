#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	check;
	int	i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
	{
		i++;
	}
	check = s1[i] - s2[i];
	return (check);
}
